    changeTeamStatus: (state, action) => {
      return state.map((player) =>
        player.playerID === action.payload
          ? { ...player, isWithoutTeam: !player.isWithoutTeam }
          : player
      );
    },
    shortListPlayer: (state, action) => {
      return state.map((player) =>
        player.playerID === action.payload
          ? { ...player, isShortListed: !player.isShortListed }
          : player
      );
    },
    userOfferedContract: (state, action) => {
      return state.map((player) =>
        player.playerID === action.payload
          ? { ...player, isOffered: !player.isOffered }
          : player
      );
    },
    setPlayers: (state, action) => {
      return action.payload;
    },
